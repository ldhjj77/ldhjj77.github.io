# 일반 가격
def price(people):
    print('{0}명 가격은 {1}원 입니다.'.format(people, people * 10000))


# 조조 할인 가격
def price_morning(people):
    print('{0}명 가격은 {1}원 입니다.'.format(people, people * 5000))


# 군인 할인 가격
def price_soldier(people):
    print('{0}명 가격은 {1}원 입니다.'.format(people, people * 2500))











